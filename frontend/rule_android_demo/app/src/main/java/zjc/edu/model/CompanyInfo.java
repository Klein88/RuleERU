package zjc.edu.model;

public class CompanyInfo {
    private String userid;
    private String companyname;
    private String companytype;
    private String companyaddress;
    private String companytel;
    private String companyregister;
    private String companyphoto;
    private String companynumber;
    private String companystatue;

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getCompanyname() {
        return companyname;
    }

    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    public String getCompanytype() {
        return companytype;
    }

    public void setCompanytype(String companytype) {
        this.companytype = companytype;
    }

    public String getCompanyaddress() {
        return companyaddress;
    }

    public void setCompanyaddress(String companyaddress) {
        this.companyaddress = companyaddress;
    }

    public String getCompanytel() {
        return companytel;
    }

    public void setCompanytel(String companytel) {
        this.companytel = companytel;
    }

    public String getCompanyregister() {
        return companyregister;
    }

    public void setCompanyregister(String companyregister) {
        this.companyregister = companyregister;
    }

    public String getCompanyphoto() {
        return companyphoto;
    }

    public void setCompanyphoto(String companyphoto) {
        this.companyphoto = companyphoto;
    }

    public String getCompanynumber() {
        return companynumber;
    }

    public void setCompanynumber(String companynumber) {
        this.companynumber = companynumber;
    }

    public String getCompanystatue() {
        return companystatue;
    }

    public void setCompanystatue(String companystatue) {
        this.companystatue = companystatue;
    }
}
